function charToColor(ch) {
  switch (ch) {
    case "#": return "#7f7f7f"; // stone
    case ".": return "#8b5a2b"; // dirt
    case ",": return "#2ecc71"; // grass
    case ":": return "#f4d03f"; // sand
    case "*": return "#ecf0f1"; // snow
    case "~": return "#3498db"; // water
    case "|": return "#8e5b3c"; // wood
    case "&": return "#27ae60"; // leaves
    case "=": return "#d0a06f"; // planks
    case "/": return "rgba(255,255,255,0.4)"; // glass
    case "$": return "#f1c40f"; // gold
    case "@": return "#1abc9c"; // diamond
    case ";": return "#e67e22"; // copper
    case ":": return "#bdc3c7"; // iron (reuse)
    default: return "magenta"; // debug
  }
}

function generateBlockTextureCanvas(blockId, size) {
  const def = BLOCK_DEFS[blockId];
  const patternName = def && def.texture ? def.texture : null;
  const pattern = patternName && BLOCK_PATTERNS[patternName];

  const canvas = document.createElement("canvas");
  canvas.width = size;
  canvas.height = size;
  const ctx = canvas.getContext("2d");

  if (!pattern) {
    ctx.fillStyle = "magenta";
    ctx.fillRect(0, 0, size, size);
    return canvas;
  }

  const rows = pattern.length;
  const cols = pattern[0].length;
  const pxW = size / cols;
  const pxH = size / rows;

  for (let y = 0; y < rows; y++) {
    const row = pattern[y];
    for (let x = 0; x < cols; x++) {
      const ch = row[x];
      ctx.fillStyle = charToColor(ch);
      ctx.fillRect(x * pxW, y * pxH, pxW, pxH);
    }
  }

  return canvas;
}
